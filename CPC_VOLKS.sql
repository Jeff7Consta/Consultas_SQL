IF OBJECT_ID('tempdb..##TAB_GERAL') IS NOT NULL DROP TABLE ##TAB_GERAL
SELECT
A.CODEMPRESA,
A.NUMCONTRDIV,
A.CODEVNTCOBADM AS COD_EVENTO,
A.CODUSERID,
B.NOME AS OPERADOR,
C.DCREVNTCOBADM,
MAX(CONVERT(DATE, A.DATHISTCOB, 103)) AS DATA_ACIONAMENTO
INTO ##TAB_GERAL
FROM ADCOBDAT..CO030 A
INNER JOIN ADCOBDAT..VW_LOGINS B ON A.CODUSERID = B.LOGIN
INNER JOIN ADCOBDAT..CO029 C ON A.CODEVNTCOBADM = C.CODEVNTCOBADM
WHERE A.CODEMPRESA BETWEEN 3001 AND 3010
AND CONVERT(DATE, A.DATHISTCOB, 103) BETWEEN  CONVERT(DATE, GETDATE()-90, 103) AND CONVERT(DATE, GETDATE(), 103)
AND A.CODEVNTCOBADM IN ('C026','C030','C036','C073','C151','C152','C153','C154','C185','C189','C193','C194','C208',
'C211','C218','C232','C239','C275','C423','C424','C425','R001','R002','R003','R004','R005','R006','R007','R008','R009',
'R010','R012','R016','R017','R019','R020','R021','R022','R027','R031','R138','R139','R140','R018')

GROUP BY 
CONVERT(DATE, DATHISTCOB, 103),
A.CODEMPRESA,
A.NUMCONTRDIV,
A.CODEVNTCOBADM,
A.CODUSERID,
B.NOME,C.DCREVNTCOBADM

ORDER BY CONVERT(DATE, DATHISTCOB, 103) ASC 

SELECT 
*
FROM ##TAB_GERAL A

ORDER BY DATA_ACIONAMENTO DESC 